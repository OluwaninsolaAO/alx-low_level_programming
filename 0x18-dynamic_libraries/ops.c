#include "ops.h"

/**
 * add - addition
 * @a: first number
 * @b: second number
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction
 * @a: first number
 * @b: second number
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - multiplication
 * @a: first number
 * @b: second number
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}


/**
 * div - division
 * @a: first number
 * @b: second number
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - remainder
 * @a: first number
 * @b: second number
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
